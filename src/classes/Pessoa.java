package classes;

/**
 *
 * @author mborges
 */
public class Pessoa {
    private int id;
    private String nome;
    private int idade;
    private String cpf;

    public Pessoa() {}
    
    public Pessoa(String nome, int idade, String cpf) {
        this.nome = nome;
        this.idade = idade;
        this.cpf = cpf;
    }

    public Pessoa(int id, String nome, int idade, String cpf) {
        this.id = id;
        this.nome = nome;
        this.idade = idade;
        this.cpf = cpf;
    }

    public String mostrarDados() {
        return "ID: " + getId() + ", Nome: " + getNome() + ", Idade: " + getIdade() + ", CPF: " + getCpf();
    }
    
    public int getId() {
        return id;
    }
    
    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }
    
}
